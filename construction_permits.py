"""## Geocoding Strategy – Summary

| | |
|---|---|
| **Primary geocoder** | swisstopo (`api3.geo.admin.ch`) – Swiss federal address register, no rate-limit issues, returns precise building centroids |
| **Fallback** | Nominatim (OpenStreetMap) – global coverage, useful for named buildings; limited to 1 req/s |
| **Validation** | Results outside the Uster municipality boundary (derived from `plots_wgs84.geojson`) are rejected |
| **Multi-address handling** | Titles with multiple streets (split by `/` or `,`) are exploded into one row per address, all sharing the same `id` |
| **Unparseable addresses** | Addresses without a house number (field names, area designations like *"Hinterbreiti Kieswerk"*, *"Chänzeli Sulzbach"*) are skipped – they cannot be resolved to a precise point |
| **Geometry type** | Parcel polygon (from spatial join with `plots_wgs84.geojson`) when available; otherwise the permit row is excluded from the GeoJSON but saved to `permits_no_geometry.csv` |
| **What did not work** | Nominatim alone: ~20% miss rate on Swiss address variants (suffixes like `1c`, `106a`), and the Uster boundary validation is strict – addresses on the edge or with slight geocoding offset were dropped |

"""