"""## 3. Address parsing

### Rules
- Strip leading `"bei "` (means *near*, still a valid address if a number follows)
- Remove everything from `Assek.` onwards (building-registry annotations)
- Split on `/` **only** when the right-hand side starts with a street-name word → two separate streets
- Split on `,` **only** when the next token looks like a new street name → two separate streets
- Drop parts with **no digit** (e.g. `"Hinterbreiti Kieswerk"`, `"Chänzeli Sulzbach"`) – cannot geocode without a house number
"""

# Street-type suffixes found in Swiss German addresses
_STREET_RE = re.compile(
    r"(strasse|str\.?|weg|gasse|platz|allee|rain|steig|stieg|halde"
    r"|holz|riet|berg|bühl|bach|wies(?:en)?|roos|matt(?:en)?"
    r"|feld|graben|ring|bogen|park|gässli|ufer|dorf)",
    re.IGNORECASE,
)

def _looks_like_street(s: str) -> bool:
    return bool(_STREET_RE.search(s))


def _expand_number_range(street: str, raw_range: str) -> list[str]:
    """Expand a house-number range into individual addresses.

    Examples:
      "Guldenenstrasse", "14-18"  → ["Guldenenstrasse 14", "Guldenenstrasse 16", "Guldenenstrasse 18"]
      "Burgstrasse",     "34-40"  → ["Burgstrasse 34", "Burgstrasse 36", "Burgstrasse 38", "Burgstrasse 40"]
      "Krämerackerstrasse", "11/11a-11d" → ["Krämerackerstrasse 11", "Krämerackerstrasse 11a", ..., "Krämerackerstrasse 11d"]
    """
    results = []

    # Case: "11/11a-11d" → base number + letter range
    m_letter_range = re.match(r'^(\d+)\s*/\s*\d+([a-z])-\d+([a-z])$', raw_range, re.IGNORECASE)
    if m_letter_range:
        base = int(m_letter_range.group(1))
        l_start = m_letter_range.group(2).lower()
        l_end = m_letter_range.group(3).lower()
        results.append(f"{street} {base}")
        for c in range(ord(l_start), ord(l_end) + 1):
            results.append(f"{street} {base}{chr(c)}")
        return results

    # Case: "14-18" or "34-40" → numeric range, step by 2 (even/odd streets)
    m_num_range = re.match(r'^(\d+)-(\d+)$', raw_range)
    if m_num_range:
        start, end = int(m_num_range.group(1)), int(m_num_range.group(2))
        step = 2 if (end - start) % 2 == 0 and end > start + 1 else 1
        for n in range(start, end + 1, step):
            results.append(f"{street} {n}")
        return results

    # Fallback: return as-is
    return [f"{street} {raw_range}"]


def _expand_same_street_numbers(street: str, numbers: list[str]) -> list[str]:
    """Given a street name and a list of number tokens, return individual addresses.

    Example: "Ackerstrasse", ["28", "32"] → ["Ackerstrasse 28", "Ackerstrasse 32"]
    """
    return [f"{street} {n}" for n in numbers if n.strip()]


def parse_addresses(title: str) -> list[str]:
    """Return a list of full addresses extracted from a permit title.
    Returns [] when no geocodable address can be extracted.
    """
    if not title:
        return []

    # ── extract the free-text part after "Bauprojekt:"
    m = re.search(r"Bauprojekt:\s*(.+)", title)
    print(m)
    if not m:
        return []
    raw = m.group(1)
    print("Bauprojekt separation", raw)

    # ── drop everything from "Assek" (house-registry annotation)
    raw = re.split(r",?\s*[Aa]ssek\.?", raw)[0].strip()
    print("Assek separation", raw)

    # ── strip leading "bei " (means "near", address is still usable)
    raw = re.sub(r"^bei\s+", "", raw, flags=re.IGNORECASE)
    # ── replace inline "bei Nr. X" → "X"  (e.g. "Im Lot bei Nr. 8" → "Im Lot 8")
    raw = re.sub(r"\s+bei\s+Nr\.?\s*", " ", raw, flags=re.IGNORECASE)
    # ── replace " und " between numbers → "/" for uniform splitting
    raw = re.sub(r"\s+und\s+(?=\d)", "/", raw, flags=re.IGNORECASE)
    print("Bei/und separation", raw)

    # ── Step 1: split on " / " (spaced slash) or plain "/"
    slash_parts = re.split(r"\s*/\s*", raw)
    print(slash_parts)

    merged: list[str] = []
    current = slash_parts[0]
    print(current)
    for part in slash_parts[1:]:
        print(part)
        if _looks_like_street(part):
            # Check if part looks like "26, Asylstrasse 25" (digit prefix before a new street)
            digit_prefix = re.match(r'^(\d[\w]*)\s*,\s*(.+)', part)
            if digit_prefix:
                current = current + "/" + digit_prefix.group(1)
                print(current.strip())
                merged.append(current.strip())
                current = digit_prefix.group(2).strip()
            else:
                print(current.strip())
                merged.append(current.strip())
                current = part.strip()
        else:
            current = current + "/" + part
            print(current)
            print("end")
    merged.append(current.strip())

    # ── Step 2: for each part, split on ", " where next token is a new street
    street_parts: list[str] = []
    for part in merged:
        sub = re.split(r",\s*", part)
        cur = sub[0]
        for s in sub[1:]:
            if _looks_like_street(s):
                street_parts.append(cur.strip())
                cur = s.strip()
        street_parts.append(cur.strip())

    # ── Step 3: expand ranges and same-street multi-numbers, then filter
    valid: list[str] = []
    for addr in street_parts:
        addr = addr.strip()
        if not addr:
            continue

        # Extract street name and the number part
        # e.g. "Guldenenstrasse 14-18" → street="Guldenenstrasse", nums="14-18"
        m_street = re.match(r'^(.+?)\s+(\d[\w/\-]*)$', addr)
        if m_street:
            street_name = m_street.group(1).strip()
            num_part    = m_street.group(2).strip()

            # numeric range: "14-18", "34-40"
            if re.match(r'^\d+-\d+$', num_part):
                expanded = _expand_number_range(street_name, num_part)
                valid.extend(f"{a}, Uster, 8610, Switzerland" for a in expanded)
                continue

            # letter range with base: "11/11a-11d"
            if re.match(r'^\d+/\d+[a-z]-\d+[a-z]$', num_part, re.IGNORECASE):
                expanded = _expand_number_range(street_name, num_part)
                valid.extend(f"{a}, Uster, 8610, Switzerland" for a in expanded)
                continue

            # same-street slash numbers: "28/32", "2/4", "25/27", "24/26"
            # only if all slash parts are purely numeric (no street words)
            slash_nums = num_part.split("/")
            if len(slash_nums) > 1 and all(re.match(r'^\d+[a-z]?$', p, re.IGNORECASE) for p in slash_nums):
                expanded = _expand_same_street_numbers(street_name, slash_nums)
                valid.extend(f"{a}, Uster, 8610, Switzerland" for a in expanded)
                continue

        # default: keep as-is if has digit or street suffix
        if re.search(r"\d", addr) or _looks_like_street(addr):
            valid.append(f"{addr}, Uster, 8610, Switzerland")

    return valid


