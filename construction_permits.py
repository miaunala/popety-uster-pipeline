def geocode_swisstopo(addr: str) -> tuple[float, float] | None:
    """Query the swisstopo location search API. Returns (lat, lon) or None.

    Validation: the street name from the query must appear in swisstopo's
    returned 'detail' field. This catches cases where swisstopo returns a
    plausible-looking point that is actually a different street.
    """
    params = {
        "type": "locations",
        "searchText": addr,
        "lang": "de",
        "limit": 1,
        "sr": "4326",
    }
    try:
        resp = requests.get(
            "https://api3.geo.admin.ch/rest/services/api/SearchServer",
            params=params,
            timeout=10,
        )
        results = resp.json().get("results", [])
        if not results:
            return None
        attrs = results[0].get("attrs", {})

        # ── Street-name validation
        # Build a street phrase from all tokens that:
        #   - don't start with a digit (removes house numbers like "47", "17a")
        #   - are longer than 2 chars (removes single-letter suffixes like "a", "b")
        # Examples:
        #   "Im Dörfli 6"       → "im dörfli"
        #   "Zürichstrasse 47"  → "zürichstrasse"
        #   "Wagerenstrasse 17a"→ "wagerenstrasse"
        street_part = addr.split(",")[0]
        street_words = " ".join(
            w for w in street_part.split()
            if not re.match(r'^\d', w) and len(w) > 2
        ).lower()
        detail = attrs.get("detail", "").lower()
        if street_words and street_words not in detail:
            print(f"swisstopo label mismatch [{addr}] → street_words: '{street_words}' not in detail: '{attrs.get('detail')}'")
            return None

        lat = attrs.get("lat")
        lon = attrs.get("lon")
        if lat is not None and lon is not None:
            return float(lat), float(lon)
    except Exception as e:
        print(f"swisstopo error [{addr}]: {e}")
    return None


def geocode_nominatim(addr: str) -> tuple[float, float] | None:
    """Nominatim fallback. Includes retry on rate-limit.

    Only accepts results with type "house" or "building" — rejects interpolated
    street-midpoint results (type "residential", "road", etc.) which Nominatim
    returns when the exact house number isn't in its database.
    """
    for attempt in range(3):
        try:
            loc = nominatim.geocode(addr, timeout=10, country_codes="ch")
            if loc:
                loc_type = loc.raw.get("type", "")
                if loc_type not in ("house", "building"):
                    print(f"Nominatim rejected [{addr}] – type='{loc_type}' (not house/building)")
                    return None
                return loc.latitude, loc.longitude
            return None
        except GeocoderQuotaExceeded:
            time.sleep(30)
        except Exception as e:
            if "429" in str(e):
                time.sleep(5 * (attempt + 1))
            else:
                print(f"Nominatim error [{addr}]: {e}")
                return None
    return None


def geocode(addr: str | None) -> tuple[float, float] | None:
    """Main geocoding function: swisstopo → Nominatim → None.
    Results are cached. Only returns coordinates inside Uster.
    """
    if not addr:
        return None
    if addr in _geocode_cache:
        return _geocode_cache[addr]

    result = None

    # ── Primary: swisstopo
    coords = geocode_swisstopo(addr)
    if coords and _in_uster(*coords):
        result = coords
        print(result)

    # ── Fallback: Nominatim
    if result is None:
        time.sleep(1.0)  # Nominatim rate-limit
        coords = geocode_nominatim(addr)
        if coords and _in_uster(*coords):
            result = coords
            print(result)

    _geocode_cache[addr] = result
    return result

def _try_geocode(addr: str) -> tuple[float, float] | None:
    """Try swisstopo then Nominatim for a single address string."""
    coords = geocode_swisstopo(addr)
    if coords and _in_uster(*coords):
        return coords
    time.sleep(0.5)
    coords = geocode_nominatim(addr)
    if coords and _in_uster(*coords):
        return coords
    return None


def _strip_letter_suffix(num: str) -> str | None:
    """'17a' → '17',  '26f' → '26',  '14' → None (no suffix to strip)"""
    m = re.match(r'^(\d+)[a-z]+$', num, re.IGNORECASE)
    return m.group(1) if m else None


def _decrement_number(street: str, num: str, max_steps: int = 5) -> tuple[tuple, str] | tuple[None, None]:
    """Try progressively lower house numbers until a geocodable address is found.
    Respects odd/even parity. Returns (coords, tried_address) or (None, None).
    """
    base_m = re.match(r'^(\d+)', num)
    if not base_m:
        return None, None
    base = int(base_m.group(1))
    step = 2 if base > 1 else 1  # keep odd/even parity

    for i in range(1, max_steps + 1):
        candidate = base - i * step
        if candidate < 1:
            break
        candidate_addr = f"{street} {candidate}, Uster, 8610, Switzerland"
        coords = _try_geocode(candidate_addr)
        if coords:
            return coords, candidate_addr
    return None, None


def geocode_with_fallback(addr: str) -> tuple[float | None, float | None, str]:
    """Full geocoding pipeline with fallbacks. Returns (lat, lon, status).

    Status values:
      "swisstopo"           – exact match via swisstopo
      "nominatim"           – exact match via Nominatim
      "needs_review:no_suffix"    – letter suffix stripped (e.g. 17a → 17), match found
      "needs_review:lower_number" – address not found, used a lower house number
      "failed"              – no match found after all fallbacks
    """
    # ── 0. Normalise house number: strip decimal part ("87.2" → "87")
    addr = re.sub(r'(\d+)\.\d+', r'\1', addr)

    # ── 1. Try exact address
    coords = geocode_swisstopo(addr)
    if coords and _in_uster(*coords):
        return coords[0], coords[1], "swisstopo"
    time.sleep(0.5)
    coords = geocode_nominatim(addr)
    if coords and _in_uster(*coords):
        return coords[0], coords[1], "nominatim"

    # Parse street name and number from addr
    street_and_num = addr.split(",")[0].strip()
    m_parts = re.match(r'^(.+?)\s+(\S+)$', street_and_num)
    if not m_parts:
        return None, None, "failed"
    street = m_parts.group(1)
    num    = m_parts.group(2)

    # ── 2. Strip letter suffix and retry (e.g. "17a" → "17", "26f" → "26")
    base_num = _strip_letter_suffix(num)
    if base_num:
        stripped_addr = f"{street} {base_num}, Uster, 8610, Switzerland"
        coords = _try_geocode(stripped_addr)
        if coords:
            print(f"  needs_review: stripped suffix [{addr}] → [{stripped_addr}]")
            return coords[0], coords[1], "needs_review:no_suffix"

    # ── 3. Decrement number (address not yet registered, try lower numbers)
    search_num = base_num if base_num else num
    coords, tried = _decrement_number(street, search_num)
    if coords:
        print(f"  needs_review: lower number [{addr}] → [{tried}]")
        return coords[0], coords[1], "needs_review:lower_number"

    # ── 4. Last resort: geocode street name alone (no house number)
    street_coords = geocode_swisstopo(f"{street}, Uster, 8610, Switzerland")
    if street_coords and _in_uster(*street_coords):
        print(f"  needs_review: street-only fallback [{addr}]")
        return street_coords[0], street_coords[1], "needs_review:street_only"

    # ── 5. Elasticsearch fuzzy search (if enabled)
    if USE_ELASTICSEARCH:
        try:
            from elasticsearch_addon import geocode_elasticsearch
            c = geocode_elasticsearch(addr)
            if c and _in_uster(*c):
                print(f"  elasticsearch fuzzy match [{addr}]")
                return c[0], c[1], "elasticsearch_fuzzy"
        except Exception as e:
            print(f"  Elasticsearch unavailable: {e}")

    return None, None, "failed"


# ── Run geocoding
to_geocode = permits_exploded["parse_status"] != "no_address"
print(f"Geocoding {to_geocode.sum()} addresses  ({(~to_geocode).sum()} skipped: no_address)...")

lats, lons, geocode_statuses = [], [], []

for _, row in tqdm(permits_exploded.iterrows(), total=len(permits_exploded), desc="Geocoding"):
    if row["parse_status"] == "no_address":
        lats.append(None)
        lons.append(None)
        geocode_statuses.append("skipped")
        continue

    lat, lon, status = geocode_with_fallback(row["project_address"])
    lats.append(lat)
    lons.append(lon)
    geocode_statuses.append(status)

permits_exploded["lat"] = lats
permits_exploded["lon"] = lons
permits_exploded["geocode_status"] = geocode_statuses

total = len(permits_exploded)
found = permits_exploded["lat"].notna().sum()
print(f"\nGeocoded: {found}/{total} ({found/total*100:.1f}%)")
print(permits_exploded["geocode_status"].value_counts(dropna=False))

print("\n── needs_review (bitte manuell nachprüfen) ──")
needs_review = permits_exploded[permits_exploded["geocode_status"].str.startswith("needs_review", na=False)]
for _, r in needs_review.iterrows():
    print(f"  [{r['geocode_status']}]  {r['project_address']}  →  ({r['lat']:.5f}, {r['lon']:.5f})")

